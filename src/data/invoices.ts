import { Invoice, InvoiceItem } from "../types";

// Helper function to generate invoice items
const createInvoiceItem = (
  id: string,
  description: string,
  quantity: number,
  unitPrice: number,
): Partial<InvoiceItem> & { id: string; description: string; quantity: number; unitPrice: number; total: number } => ({
  id,
  description,
  quantity,
  unitPrice,
  unit_price: unitPrice,
  total: quantity * unitPrice,
  invoice_id: "",
  created_at: new Date().toISOString(),
});

